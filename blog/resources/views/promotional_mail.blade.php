<p>Over the past 4 years in the process of building this community, we have been undertaking a lot of events through which many of our members have been able to find business connections, co-founders, customers and even investment. All of this has been possible because of the community and communication between them.</p>

<p>We find that opportunities emerge when people talk.</p>

<p>We have been keeping this communication open through various channels including WhatsApp, Facebook, Meetup and many other platforms. We know that the discussions that happen here are incredibly helpful. But we are also aware that it does not bring everyone together. If you can find 1 customer through discussions with 100 members, imagine the possibility if all 20,000 were on a single platform!</p>

<p>We wished to take this beyond the confines of distinct platforms and their limitations and with that in mind we decided to launch the Members Platform. To make conversations possible within the community and connections easier.</p>

<p>We hope to be able to make the entrepreneurial journey a little easier.</p>

<p>We <a href="https://members.startupsclub.org/">launched this platform</a> only for our Pioneer Members in April 2017. It gave us time to understand its usage and the features that need to be built.</p>
The platform currently has:
<ul>
   <li> Member Newsfeed – Post and comment on others posts</li>
    <li>Messaging – One to One connection between members</li>
   <li> Find - Search members based on their profiles, interests and specializations</li>
    <li>Ask – Anything that is challenging you in your entrepreneurial journey (Coming Soon)</li>
    <li>Events – Members (experts) will be able to post their own events (Coming Soon)</li>
</ul>
<p>We are opening this platform up to all of the members who are part of Startups Club. We want you to use it and give us your feedback on it. You could also suggest features that we could build out on the platform that may be useful to you.</p>

<p>We are building this for you, it is yours! We are here to enable, please let us know how we can make it better for you.</p>

